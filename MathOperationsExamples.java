package com.digitek.mathoperators;

public class MathOperationsExamples {

	/*
	 * 
	 * Math operators: +, -, *, / ,%
	 *  
	 */
	
	
	public static void main(String[] args) {
		addition();
		substraction();
		multiplication();
		division();
		modulus();
	}
	
	public static void addition() {
		
		int a = 10;
		int b = 20;
		
		int result = a + b;
		System.out.println("addition() value of result:" + result);
		
	}
	
	public static void substraction() {
		
		int a = 10;
		int b = 20;
		
		int result = a - b;
		System.out.println("substraction() value of result:" + result);
		
	}
	
	public static void multiplication() {
		
		int a = 10;
		int b = 20;
		
		int result = a * b;
		System.out.println("multiplication value of result:" + result);
		
	}
	
	public static void division() {
		
		int a = 10;
		int b = 20;
		
		int result = a / b;
		System.out.println("division() value of result:" + result);
		
	}
	
	public static void modulus() {
		
		int a = 10;
		int b = 20;
		
		int result = a + b;
		System.out.println("modulus() value of result:" + result);
		
	}
	

}
